from enum import Enum
from typing import Any, Dict, List, Optional
from pydantic import BaseModel, Field

class EngineStatus(str, Enum):
    ACTIVE = "ACTIVE"
    NO_SIGNIFICANT_FINDINGS = "NO_SIGNIFICANT_FINDINGS"
    ERROR = "ERROR"

class EngineResult(BaseModel):
    engine_name: str = Field(..., description="Name of the analytics engine")
    status: EngineStatus = Field(..., description="Lifecycle status of the engine's evaluation")
    confidence: float = Field(0.0, description="Confidence score in percent (0 to 100)")
    risk_score: float = Field(0.0, description="Risk sub-score calculated by the engine (0 to 100)")
    findings: List[str] = Field(default_factory=list, description="Textual summaries of key findings")
    supporting_evidence: List[str] = Field(default_factory=list, description="Referenced evidence elements (e.g. event IDs)")
    shared_signals: List[str] = Field(default_factory=list, description="Signals shared with other engines via manager")
    recommendations: List[str] = Field(default_factory=list, description="Advisory recommendations generated by the engine")
    engine_metadata: Dict[str, Any] = Field(default_factory=dict, description="Generic metadata for future extension")

    # Optional internal assessment objects
    behaviour_assessment: Optional[Dict[str, Any]] = None
    fraud_assessment: Optional[Dict[str, Any]] = None
    cyber_assessment: Optional[Dict[str, Any]] = None
    quantum_assessment: Optional[Dict[str, Any]] = None
